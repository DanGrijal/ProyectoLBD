    private void comboxAntenaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboxAntenaItemStateChanged
        // TODO add your handling code here:
        System.out.println("proyectolbd.frmMenuPrincipal.comboxAntenaItemStateChanged()" + evt.getItem());
        int antenaSeleccionada = comboxAntena.getSelectedIndex();

        // Validar que el índice es válido
        if (antenaSeleccionada >= 0 && antenaSeleccionada < antenas.size()) {
            // Obtener el objeto correspondiente del ArrayList
            Antena antena = antenas.get(antenaSeleccionada);

            // Mostrar información o realizar acciones
            mostrarTarifas(antena.getIdAntena());
        }
    }//GEN-LAST:event_comboxAntenaItemStateChanged

    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        // TODO add your handling code here:
        int antenaSeleccionada = comboxAntena.getSelectedIndex();
        int quickpassSeleccionado = comboxQuickpass.getSelectedIndex();

        Antena antena = null;
        Quickpass quickpass = null;
        // Validar que el índice es válido
        if (antenaSeleccionada >= 0 && antenaSeleccionada < antenas.size()) {
            // Obtener el objeto correspondiente del ArrayList
            antena = antenas.get(antenaSeleccionada);
        }

        if (quickpassSeleccionado >= 0 && quickpassSeleccionado < usuarioActualQuickpasses.size()) {
            // Obtener el objeto correspondiente del ArrayList
            quickpass = usuarioActualQuickpasses.get(quickpassSeleccionado);
        }

        if (antena != null && quickpass != null) {
            procesarPase(antena, quickpass);
        }

    }//GEN-LAST:event_btnProcesarActionPerformed

