    private void llenarListaUsuarios() {
        usuarios = conexion.getUsuarios();

        for (Usuario usuario : usuarios) {
            listUsuarioModel.addElement(usuario.getNombre());
        }
    }

    private void mostrarInfoUsuario(Usuario usuario) {
        panelInfoUsuario.setVisible(true);
        usuarioActualQuickpasses = conexion.getQuickpass(usuario.getIdUsuario());
        comboxQuickpass.setModel(comboxQuickpassModel);

        comboxQuickpassModel.removeAllElements();

        for (Quickpass quickpass : usuarioActualQuickpasses) {
            comboxQuickpassModel.addElement("ID: "+quickpass.getIdQuickpass()+" - "+quickpass.getNombreBanco());
        }
    }

    private void mostrarAntenas() {
        antenas = conexion.getAntena();
        comboxAntena.setModel(comboxAntenaModel);

        for (Antena antena : antenas) {
            comboxAntenaModel.addElement(antena.getRutaPeaje() + " (" + antena.getIdAntena() + ")");
        }
    }

