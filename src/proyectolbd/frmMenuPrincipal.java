    private void mostrarTarifas(int idAntena) {
        lblTarifas.setVisible(false);
        ArrayList<Tarifas> tarifas = conexion.getMontoAntena(idAntena);

        String tarifasString = "<html>TARIFAS<br>";
        for (Tarifas tarifa : tarifas) {
            tarifasString += tarifa.getDescripcionTipoVehiculo() + " > $" + tarifa.getMonto() + "<br>";
        }
        tarifasString += "</html>";

        lblTarifas.setText(tarifasString);
    }

    private void procesarPase(Antena antena, Quickpass quickpass) {
        int resultado = conexion.procesarPase(antena.getIdAntena(), quickpass.getIdQuickpass());
        switch (resultado) {
            case 1:
                lblResultado.setText("<html>RECHAZADA<br>Su QuickPass no puede ser encontrado en los peajes</html>");
                break;
            case 2:
                lblResultado.setText("<html>RECHAZADA.<br>Su QuickPass está inactivo, por favor contacte a su proveedor</html>");
                break;
            case 3:
                lblResultado.setText("<html>RECHAZADO.<br>EL tipo de Vehiculo no fue encontrado, contacte a su proveedor</html>");
                break;
            case 4:
                lblResultado.setText("<html>RECHAZADO.<br>No se puede encontrar el costo del peaje</html>");
                break;
            case 5:
                lblResultado.setText("<html>PASE AUTORIZADO.<br>Que disfrute su día, y maneje con cuidado</html>");
                break;
            case 6:
                lblResultado.setText("<html>PASE RECHAZADO</html>");
                break;
            case -1:
                lblResultado.setText("<html>Error de conexion</html>");
                break;
            default:
                throw new AssertionError();
        }
    }

